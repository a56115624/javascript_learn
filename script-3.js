//如何使用數字,數字的用法

document.write(6.2)
document.write("</br>");

document.write(8%5);
document.write("</br>");
var number = -6;
document.write(Math.abs(number));
document.write("</br>");
// max 找出最大的數
document.write(Math.max(11,2,8,4,9,5,156,4846,4846145,4158486));

document.write("</br>");
// round取整數
var number = 2.3;
document.write(Math.round(number));
document.write("</br>");
// pow 某數的幾次方
document.write(Math.pow(2,3));
document.write("</br>");
// sqrt 開根號
document.write(Math.sqrt(36));

document.write("</br>");
// random 隨機回傳0~1之間的整數
document.write(Math.round(Math.random()*10));
